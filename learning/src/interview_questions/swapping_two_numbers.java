package interview_questions;

public class swapping_two_numbers {
    public static void main(String[] args) {

        int a = 2, b = 3, c;
        c = a;
        a = b;
        b = c;
        System.out.println("a is =" + " " + a);
        System.out.println("b is =" + " " + b);

    }
}
